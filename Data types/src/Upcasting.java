
public class Upcasting {
	 public static void main(String args[]) {
	      
	      int k = 1234;
	      long l = k;
	      float m = l;
	      double n = m;
	     
	      System.out.println("int value : "+k);
	      System.out.println("long value : "+l);
	      System.out.println("float value : "+m);
	      System.out.println("double value : "+n);
	   }

}
